function toggleVisibility(id) {
  var x = document.getElementById(id);
  if (x.classList.contains("visible")) {
    x.classList.remove("visible");
  } else {
    x.classList.add("visible");
  }
}
function autoclicker() {
  toggleVisibility("autoclicker");
}

